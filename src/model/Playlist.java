package model;

public class Playlist {
	String playlistName;

	public Playlist(String playlistName) {
		super();
		this.playlistName = playlistName;
	}

	public String getPlaylistName() {
		return playlistName;
	}

	public void setPlaylistName(String playlistName) {
		this.playlistName = playlistName;
	}
	
	
}
